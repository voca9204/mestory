// 종합 미션을 위한 믹싱 함수
export const generateMixedMissions = (selectedTypes: MissionType[], duration: number = 30): Record<number, string> => {
  const mixedMissions: Record<number, string> = {}
  
  // MIXED와 DECLUTTER 제외하고 실제 미션 타입들만 필터링
  const actualTypes = selectedTypes.filter(type => type !== MissionType.MIXED && type !== MissionType.DECLUTTER)
  
  if (actualTypes.length === 0) {
    // 선택된 타입이 없으면 기본 미션 반환
    for (let day = 1; day <= duration; day++) {
      mixedMissions[day] = `${day}일차 미션`
    }
    return mixedMissions
  }
  
  // 각 타입에서 골고루 미션을 가져오기 위한 로직
  for (let day = 1; day <= duration; day++) {
    const typeIndex = (day - 1) % actualTypes.length
    const selectedType = actualTypes[typeIndex]
    const typeMissions = MISSIONS_BY_TYPE[selectedType]
    
    // 해당 타입의 미션 중에서 순환하며 선택
    const missionDay = ((day - 1) % 30) + 1
    const mission = typeMissions[missionDay] || `${selectedType} 미션`
    
    mixedMissions[day] = `${MISSION_TYPE_INFO[selectedType].icon} ${mission}`
  }
  
  return mixedMissions
}

